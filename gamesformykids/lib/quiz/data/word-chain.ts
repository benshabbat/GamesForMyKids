/** Index words by their first letter for O(1) lookup */
export const WORD_INDEX: Record<string, string[]> = {};
for (const word of WORD_BANK) {
  const first = word[0];
  if (!first) continue;
  if (!WORD_INDEX[first]) WORD_INDEX[first] = [];
  WORD_INDEX[first].push(word);
}

/**
 * Returns the last letter of a Hebrew word.
 * Strips trailing spaces (multi-word entries) and resolves sofit forms to base forms.
 */
export function getLastLetter(word: string): string {
  const cleaned = word.trimEnd();
  const last = cleaned[cleaned.length - 1] ?? '';
  // Map sofit (final) letter forms to the base form used as first-letter keys
  const SOFIT_MAP: Record<string, string> = {
    'ך': 'כ',
    'ם': 'מ',
    'ן': 'נ',
    'ף': 'פ',
    'ץ': 'צ',
  };
  return SOFIT_MAP[last] ?? last;
}
